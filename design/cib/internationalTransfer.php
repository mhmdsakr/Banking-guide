    <div class="section-title">
        <h5 style="font-weight:bold">How do you make an international wire transfer or Global Money Transfer with CIBC?</h5>
    </div>
    <div>
        <h6>To make a Global Money Transfer with CIBC, you’ll need</h6>
        <ul>
            <li>Enough funds in your account</li>
            <li>The recipient’s full address</li>
            <li>The full name of the recipient’s bank</li>
            <li>
                The location of the recipient’s bank, including country, city, province or state, postal code or area code
            </li>
            <li>The recipient’s bank account number</li>
            <li>The reason for the transfer</li>
        </ul>
        <p>
            The info varies slightly if you’re sending money to China. So check out CIBC’s GMT info page for more specifics.
        </p>
        <br>
        <h6>To send a wire transfer with CIBC, you’ll need:</h6>
        <ul>
            <li>The recipient’s full name and their email address we’ll email them, and they can fill out their local banking details from there</li>
        </ul>
    </div>
    <div class="section-title">
        <h5 style="font-weight:bold">What do I need, or what should I give to the sender in order to receive an international bank transfer with CIBC?</h5>
    </div>
    <div>
        <h6>To receive an international money transfer to CIBC, you’ll need to give the sender:</h6>
        <ul>
            <li>CIBC’s BIC/SWIFT code</li>
            <li>Your account number</li>
            <li>Your account name</li>
            <li>Your 5 digit transit number (including zeros)</li>
            <li>Your home address</li>
            <li>Your bank address</li>
            <li>You may need to agree with the sender about who will pay the fees associated with the transfer</li>
        </ul>
        <p>
            You can log into CIBC mobile or online banking to find this information, or call 1-800-465-2422 toll-free.
        </p>
    </div>
