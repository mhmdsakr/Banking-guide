        <br>
        <br>
        <br>
        <h2 style="color:blue">Subscribing to the e-statement service is available through the Online Banking service or SMS!</h2>
        <br>
        <p>An e-statement is a fast and easy way to access your bank and credit card statements through the Internet Banking service.</p>
        <br>
        <p>You can now apply for the e-statement service in multiple ways; either through an SMS or online via the Internet Banking service or Mobile Banking app.</p>
        <br>
        <p>Upon subscribing to the service, you will receive an email notification once your e-statement is issued.</p>
        <br>
        <p>The issued e-statement is considered the equivalent of the paper statement.</p>
        <br>
        <p style="color:blue">You can view and download the e-statement to your personal computer or mobile phone, in the below formats:</p>

        <ul>
            <li>Word</li>
            <li>PDF</li>
            <li>Excel</li>
        </ul>

        <h2 style="color:blue">The subscription steps through the Internet Banking service or the Mobile Banking App:</h2>
        <p>1.Open your Internet Banking or Mobile Banking account through your mobile phone</p>
        <p>2.Press "Requests"</p>
        <p>3.Select “E-Statement Enrollment” if you are using internet banking service or "Subscribe to E-statement" in case you are using the Mobile Banking App</p>
        <p>4.Confirm the registered email and read and agree to the terms and conditions of service</p>
        <br>
        <br>
        <h2 style="color:blue">The subscription steps through SMS:</h2>
        <h6>Subscribing through SMS is only valid for customers who have signed the Electronic Service Agreement Form.</h6>
        <br>
        <p>1.Send the number (04) followed by your National ID number in an SMS to 1491</p>
        <br>
        <p>2.You will receive an SMS with a one-time password (OTP), the email registered with the Bank, and the terms and conditions of the service</p>
        <br>
        <p>3.Reply with the OTP within 10 minutes of receiving the SMS to proceed</p>
        <br>
        <p>You will receive an SMS confirming your subscription to the service</p>
        <br>
        <br>
        <h6 style="color:blue">To apply successfully via SMS, make sure of the following:</h6>
        <ul>
            <li>Your information at the Bank is valid</li>
            <li>Your email is correct</li>
            <li>Your National ID is valid</li>
            <li>The SMS is sent using your mobile number that is registered with the Bank</li>
        </ul>
        <br>
        <br>
        <p>If you do not meet any of the above conditions, you will receive an SMS stating that the request has not been completed and asking you to contact CIB customer service on 19666 to know the reason.</p>
        <br>
        <br>
        <p>Additionally, if you entered an incorrect National ID number, you will receive an SMS stating that the request has not been completed. You can repeat the steps of the subscription process to try again.</p>
        <br>
        <br>
        <p>If you will apply at any CIB branch, please make sure the email that you want to receive your e-statement notifications on is entered correctly</p>
        <br>
        <br>
        <h6 style="color:blue">Unsubscribing from the service</h6>
        <p>To unsubscribe from the service, please visit the nearest CIB branch to submit your request.</p>
        <p>Once you unsubscribe from the service, you will receive paper statements again at your registered address.</p>
